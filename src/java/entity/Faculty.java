package entity;

/**
 *
 * @author Ark
 */
public class Faculty {

    private int id;
    private String code, name, designation;

    public Faculty() {
    }

    public Faculty(int id) {
        this.id = id;
    }

    public Faculty(int id, String code, String name, String designation) {
        this.id = id;
        this.code = code;
        this.name = name;
        this.designation = designation;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDesignation() {
        return designation;
    }

    public void setDesignation(String designation) {
        this.designation = designation;
    }

}
